-- USERS 테이블이 있으면 드롭한다
DROP TABLE USERS IF EXISTS;

-- NETTY_EXAMPLE_DB 생성후 관리자권한 설정
CREATE SCHEMA NETTY_EXAMPLE_DB AUTHORIZATION DBA;

-- 생성된 NETTY_EXAMPLE_DB로 이동하는 명령 
-- 이 명령 이후 실행되는 쿼리는 NETTY_EXAMPLE_DB로 전달된다
SET SCHEMA NETTY_EXAMPLE_DB;
 

-- 사용자 정보 생성 스크립트
CREATE TABLE USERS (
    USERNO INT GENERATED BY DEFAULT AS IDENTITY 
          (START WITH 1, INCREMENT BY 1) NOT NULL,
    USERID VARCHAR(45) NOT NULL,
    USERNAME VARCHAR(45) NOT NULL,
    PASSWORD VARCHAR(45) NOT NULL,
    PRIMARY KEY (USERID)
);

